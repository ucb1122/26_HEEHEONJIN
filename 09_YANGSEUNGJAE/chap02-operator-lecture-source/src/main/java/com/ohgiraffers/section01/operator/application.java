package com.ohgiraffers.section01.operator;

public class application {
    public static void main(String[] args) {

        int num1 = 20;
        int num2 = 7;

        System.out.println("num1 + num2 = " + (num1 + num2));
        System.out.println("num1 - num2 = " + (num1 - num2));
        System.out.println("num1 * num2 = " + (num1 * num2));
        System.out.println("num1 / num2 = " + (num1 / num2));
        System.out.println("num1 % num2 = " + (num1 % num2));

        int num =12;
        System.out.println("num = " + num);

        num = num + 3;
        System.out.println("num = " + num);
        num += 3;
        System.out.println("num = " + num);
        num -= 5;
        System.out.println("num = " + num);
        num *= 2;
        System.out.println("num = " + num);
        num /= 2;
        System.out.println("num = " + num);


    }
}
